package entity;

public class Car {
	private String brand;
	private String series;
	private String type;
	private int price;
	private int brandValue;
	private int seriesValue;
	private int typeValue;

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getSeries() {
		return series;
	}

	public void setSeries(String series) {
		this.series = series;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getBrandValue() {
		return brandValue;
	}

	public void setBrandValue(int brandValue) {
		this.brandValue = brandValue;
	}

	public int getSeriesValue() {
		return seriesValue;
	}

	public void setSeriesValue(int seriesValue) {
		this.seriesValue = seriesValue;
	}

	public int getTypeValue() {
		return typeValue;
	}

	public void setTypeValue(int typeValue) {
		this.typeValue = typeValue;
	}

}
